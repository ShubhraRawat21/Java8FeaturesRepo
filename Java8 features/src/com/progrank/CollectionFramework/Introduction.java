package src.com.progrank.CollectionFramework;

public class Introduction {
    // 1. Collection - Interface
    // Collections class implements this interface and provides various methods to
    // work with the collections

    // List(I)
    /*
     * Insertion order is preserved
     * Duplicates are allowed
     * Classes- ArrayList, LinkedList, Vector, Stack
     */

    // Set(I)
    /*
     * Insertion order is not preserved
     * Duplicates are not allowed
     * Classes- HashSet, LinkedHashSet
     */

    // Queue(I)
    /*
     * Use when FIFO is required
     * Classes - PriorityQueue
     */

    // 2. Map(I) - Map is not a part of Collection interface
    // Key-Value pair- Key is an object and value is also an object

    // Classes - HashMap(C), LinkedHashMap(C), HashTable(C)

}
